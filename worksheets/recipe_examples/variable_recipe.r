## Blueberry muffin recipe (makes 12):
## 3 cups all-purpose flour
## 2 tablespoons sugar
## 1.5 tablespoons baking powder
## 0.5 teaspoon salt
## 3 eggs
## 0.5 cup butter
## 1 cup buttermilk
## 1 teaspoon vanilla extract
## 2 cups frozen blueberries
##---------------------------------------------------------

# I want to make 9 muffins instead. What is my new recipe?

multiplier <- 9/12

# flour (cups)
3 * multiplier

# sugar (tablespoons)
2 * multiplier

# baking powder (tablespoons)
1.5 * multiplier

# salt (teaspoons)
0.5 * multiplier

# eggs
3 * multiplier

# butter (cups)
0.5 * multiplier

# buttermilk (cups)
1 * multiplier

# vanilla extract (teaspoons)
1 * multiplier

# frozen blueberries (cups)
2 * multiplier
